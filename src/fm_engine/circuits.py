CIRCUITS_BY_CODE: dict[str, Circuit] = {circuit.code: circuit for circuit in CALENDAR_2026}


def circuit_by_code(code: str) -> Circuit:
    """Il circuito con il codice dato; KeyError con messaggio chiaro se assente."""
    try:
        return CIRCUITS_BY_CODE[code]
    except KeyError:
        raise KeyError(f"unknown circuit code: {code!r}") from None
