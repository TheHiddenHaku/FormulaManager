"""Dati statici dei circuiti e Calendario 2026 (FOR-8).

Il motore e' puro (ADR 0002) e non legge dal database: i 24 circuiti del
Calendario 2026 vivono qui come dataclass immutabili. supabase/seed.sql
e' il mirror SQL di questi stessi valori per la persistenza: ogni
modifica ai numeri va riportata in entrambi i posti.

Il tempo base sul giro (secondi), i pesi sui 6 Attributi vettura
(0.0-1.0), la severita' gomme (1-5), la difficolta' di sorpasso (1-5),
le probabilita' di Safety car e pioggia e le mescole nominate sono
valori di partenza plausibili, da tarare con l'harness di bilanciamento
(T2.4.1).
"""

from dataclasses import dataclass
from datetime import date

# How much each of the 6 car attributes counts on the lap, keyed by the
# attribute names of fm_engine.world.models.CAR_ATTRIBUTES.
_WEIGHT_FIELDS: tuple[str, ...] = (
    "engine_power_weight",
    "downforce_weight",
    "aero_efficiency_weight",
    "mechanical_grip_weight",
    "tyre_management_weight",
    "reliability_weight",
)


@dataclass(frozen=True)
class Circuit:
    """Un circuito del Calendario, con profilo prestazionale e ambientale.

    Replica la riga della tabella SQL circuits: pesi sugli Attributi
    vettura, severita' gomme, probabilita' Safety car, profilo meteo e
    le 3 Mescole nominate per il GP.
    """

    code: str
    name: str
    country: str
    locality: str
    length_metres: int
    race_laps: int
    calendar_order: int
    race_date_2026: date
    weekend_format_2026: str
    # Realistic reference race lap of the base season, in seconds: the
    # additive base of the lap time model (fm_engine.laptime).
    base_lap_seconds: float
    engine_power_weight: float
    downforce_weight: float
    aero_efficiency_weight: float
    mechanical_grip_weight: float
    tyre_management_weight: float
    reliability_weight: float
    tyre_severity: int
    # Overtaking difficulty on track, scale 1 (easy: Monza) - 5 (nearly
    # impossible: Monaco). Modulates duel resolution in the race engine.
    overtaking_difficulty: int
    safety_car_probability: float
    weather_profile: str
    rain_probability: float
    hard_compound: str
    medium_compound: str
    soft_compound: str

    @property
    def attribute_weights(self) -> dict[str, float]:
        """I pesi del circuito indicizzati per nome di Attributo vettura."""
        return {
            attribute: getattr(self, field)
            for attribute, field in zip(
                (
                    "engine_power",
                    "downforce",
                    "aero_efficiency",
                    "mechanical_grip",
                    "tyre_management",
                    "reliability",
                ),
                _WEIGHT_FIELDS,
                strict=True,
            )
        }

    @property
    def nominated_compounds(self) -> tuple[str, str, str]:
        """Le 3 Mescole da asciutto nominate, dalla piu' dura alla piu' morbida."""
        return (self.hard_compound, self.medium_compound, self.soft_compound)


